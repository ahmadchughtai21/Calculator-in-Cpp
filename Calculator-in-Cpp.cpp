#include <iostream>

using namespace std;

int main() {
    float n1, n2;

    cout << "Enter first number: ";
    cin >> n1;
    cout << "Enter second number: ";
    cin >> n2;

    cout << "N1 + N2 = " << n1+n2 <<endl ;
    cout << "N1 - N2 = " << n1-n2 <<endl ;
    cout << "N1 * N2 = " << n1*n2 <<endl ;
    cout << "N1 / N2 = " << n1/n2 <<endl ;

    return 0;
}
